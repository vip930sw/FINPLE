import { execFileSync } from "node:child_process";
import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";

const repositoryUrl = "https://github.com/vip930sw/FINPLE";
const sourceRef = process.argv[2] || "origin/main";
const outputPath = resolve(process.argv[3] || "src/data/updateWorklogData.js");
const todayInKorea = new Intl.DateTimeFormat("en-CA", {
  timeZone: "Asia/Seoul",
  year: "numeric",
  month: "2-digit",
  day: "2-digit",
}).format(new Date());
const fromDate = process.argv[4] || "2026-05-10";
const toDate = process.argv[5] || todayInKorea;
const SUPPLEMENTAL_PR_RECORDS = [
  {
    date: "2026-06-20",
    displayDate: "2026.06.20",
    category: "문서·운영",
    type: "pr",
    number: 214,
    sha: "",
    title: "업데이트 페이지를 날짜·PR·작업별 업무일지로 개편",
    url: `${repositoryUrl}/pull/214`,
  },
];

const CATEGORY_RULES = [
  ["교육 계정", /education|교육/i],
  ["문의·알림", /inquir|support|notification|email alert|reply|문의|알림/i],
  ["결제·구독", /payment|billing|subscription|refund|pricing|toss|결제|구독|환불/i],
  ["인증·계정", /auth|login|oauth|google|kakao|naver|password|account withdrawal|signup|session|로그인|계정/i],
  ["투자 MBTI", /mbti|investment profile|투자성향/i],
  ["데이터·스크리너", /screener|candidate|ticker|csv|dividend|metric|overlay|data source|lookup|배당|스크리너/i],
  ["시뮬레이터·분석", /simulator|portfolio|detail|chart|target weight|compare|analysis|시뮬레이터|포트폴리오/i],
  ["MY PAGE", /my page|mypage/i],
  ["문서·운영", /document|handoff|checklist|runbook|monitor|release|roadmap|policy note|문서|인수인계/i],
  ["UI·콘텐츠", /about|home|footer|header|navigation|route|layout|copy|style|logo|spacing|button|mobile|ui/i],
];

function runGit(args) {
  return execFileSync("git", args, {
    encoding: "utf8",
    maxBuffer: 16 * 1024 * 1024,
  }).trim();
}

function getCategory(title) {
  return CATEGORY_RULES.find(([, pattern]) => pattern.test(title))?.[0] || "기타 작업";
}

function toKoreanDate(isoDate) {
  return new Intl.DateTimeFormat("ko-KR", {
    timeZone: "Asia/Seoul",
    year: "numeric",
    month: "2-digit",
    day: "2-digit",
  }).format(new Date(isoDate)).replace(/\. /g, ".").replace(/\.$/, "");
}

function toIsoDay(isoDate) {
  const parts = new Intl.DateTimeFormat("en-CA", {
    timeZone: "Asia/Seoul",
    year: "numeric",
    month: "2-digit",
    day: "2-digit",
  }).formatToParts(new Date(isoDate));
  const values = Object.fromEntries(parts.map((part) => [part.type, part.value]));
  return `${values.year}-${values.month}-${values.day}`;
}

const rawLog = runGit([
  "log",
  sourceRef,
  "--first-parent",
  `--since=${fromDate}T00:00:00+09:00`,
  `--until=${toDate}T23:59:59+09:00`,
  "--pretty=format:%H%x1f%P%x1f%aI%x1f%s%x1e",
]);

const records = rawLog
  .split("\x1e")
  .map((entry) => entry.trim())
  .filter(Boolean)
  .map((entry) => {
    const [sha, parentText, authoredAt, subject] = entry.split("\x1f");
    const parents = parentText.split(" ").filter(Boolean);
    const prMatch = subject.match(/(?:pull request|PR)\s+#(\d+)/i);
    const prNumber = prMatch ? Number(prMatch[1]) : null;
    const sourceTitle = prNumber && parents[1]
      ? runGit(["show", "-s", "--format=%s", parents[1]])
      : subject;

    return {
      date: toIsoDay(authoredAt),
      displayDate: toKoreanDate(authoredAt),
      category: getCategory(sourceTitle),
      type: prNumber ? "pr" : "commit",
      number: prNumber,
      sha: sha.slice(0, 7),
      title: sourceTitle,
      url: prNumber
        ? `${repositoryUrl}/pull/${prNumber}`
        : `${repositoryUrl}/commit/${sha}`,
    };
  });

for (const supplementalRecord of SUPPLEMENTAL_PR_RECORDS) {
  const alreadyIncluded = records.some(
    (record) => record.type === "pr" && record.number === supplementalRecord.number
  );
  if (!alreadyIncluded) records.push(supplementalRecord);
}

const days = new Map();
for (const record of records) {
  if (!days.has(record.date)) {
    days.set(record.date, {
      date: record.date,
      displayDate: record.displayDate,
      records: [],
    });
  }
  days.get(record.date).records.push(record);
}

const worklog = Array.from(days.values())
  .sort((a, b) => b.date.localeCompare(a.date))
  .map((day) => {
    const categoryMap = new Map();
    for (const record of day.records) {
      if (!categoryMap.has(record.category)) categoryMap.set(record.category, []);
      categoryMap.get(record.category).push(record);
    }

    const tasks = Array.from(categoryMap.entries()).map(([category, items]) => ({
      category,
      items,
    }));
    const prCount = day.records.filter((record) => record.type === "pr").length;
    const commitCount = day.records.length - prCount;

    return {
      date: day.date,
      displayDate: day.displayDate,
      summary: tasks.map((task) => task.category).join(" · "),
      prCount,
      commitCount,
      tasks,
    };
  });

const generatedAt = new Date().toISOString();
const fileContents = `// This file is generated by scripts/generate-updates-worklog.mjs.
// Do not edit it manually. Update Git history, then rerun the generator.

export const WORKLOG_META = ${JSON.stringify({
  fromDate,
  toDate,
  generatedAt,
  sourceRef,
  totalDays: worklog.length,
  totalPullRequests: records.filter((record) => record.type === "pr").length,
  totalDirectCommits: records.filter((record) => record.type === "commit").length,
}, null, 2)};

export const UPDATE_WORKLOG = ${JSON.stringify(worklog, null, 2)};
`;

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, fileContents, "utf8");
console.log(`Generated ${outputPath}`);
console.log(`Days: ${worklog.length}, PRs: ${records.filter((record) => record.type === "pr").length}, direct commits: ${records.filter((record) => record.type === "commit").length}`);

function LegalDocumentPage({ eyebrow, title, description, sections }) {
  return (
    <main className="accountPage legalPage standaloneLegalPage">
      <section className="accountHero">
        <p className="sectionLabel">{eyebrow}</p>
        <h1>{title}</h1>
        <p>{description}</p>
      </section>

      <section className="accountCard legalDocumentCard">
        {sections.map((section) => (
          <article key={section.title} className="legalDocumentSection">
            <h2>{section.title}</h2>
            {section.paragraphs.map((paragraph, index) => (
              <p key={index}>{paragraph}</p>
            ))}
            {section.items ? (
              <ul>
                {section.items.map((item) => <li key={item}>{item}</li>)}
              </ul>
            ) : null}
          </article>
        ))}
      </section>
    </main>
  );
}

